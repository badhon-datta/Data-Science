import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
df = pd.read_csv('/content/drive/MyDrive/Badhon/heightweight.csv')

"""Asn to question no 1"""

plt.figure(figsize=(10, 6))
sns.scatterplot(data=df, x='ageYear', y='heightIn')
plt.title('Scatterplot of Age vs Height')
plt.xlabel('Age (years)')
plt.ylabel('Height (inches)')
plt.show()

"""Ans to the question no 2"""

plt.figure(figsize=(10, 6))
sns.scatterplot(data=df, x='ageYear', y='heightIn', hue='sex', style='sex', markers=['o', 's'])
plt.title('Scatterplot of Age vs Height by Gender')
plt.xlabel('Age (years)')
plt.ylabel('Height (inches)')
plt.legend(title='Gender')
plt.show()

"""Ans to the question no 3"""

plt.figure(figsize=(10, 6))
sns.boxplot(data=df, x='sex', y='heightIn')
plt.title('Boxplot of Heights by Gender')
plt.xlabel('Gender')
plt.ylabel('Height (inches)')
plt.show()

"""Ans to the question no 4"""

plt.figure(figsize=(10, 6))
sns.scatterplot(data=df, x='ageYear', y='heightIn', size='weightLb', hue='sex', sizes=(20, 200), alpha=0.7, legend=False)
plt.title('Scatterplot of Age vs Height with Weight as Point Size')
plt.xlabel('Age (years)')
plt.ylabel('Height (inches)')
plt.show()

"""Ans to the questio no 5"""

plt.figure(figsize=(10, 6))
sns.histplot(data=df, x='heightIn', hue='sex', element='step', stat='density',  bins=30)
plt.title('Histogram of Heights by Gender')
plt.xlabel('Height (inches)')
plt.ylabel('Density')
plt.show()

"""Ans to the question no 6"""

plt.figure(figsize=(10, 6))
sns.histplot(data=df, x='heightIn', hue='sex', kde=True, element='step', stat='density', common_norm=False, bins=30)
plt.title('Height Distribution with KDE by Gender')
plt.xlabel('Height (inches)')
plt.ylabel('Density')
plt.show()

"""Ans to the question no 7"""

g = sns.FacetGrid(df, col='sex', height=5, aspect=1)
g.map(sns.histplot, 'heightIn', bins=30, kde=True, color='blue', stat='density')
g.set_titles(col_template='{col_name} Height Distribution')
plt.show()